public class Lc88_merge_sorted_array {

    public static void merge(int[] nums1, int m, int[] nums2, int n) {
        if (nums1 == null
                || nums2 == null
                || nums1.length == 0
                || nums2.length == 0
                || nums1.length + nums2.length < m + n) {
            return;
        }



    }

}
